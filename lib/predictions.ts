/**
 * Prediction persistence layer.
 *
 * Architecture:
 *   KV (Upstash Redis)      — hot read cache, optional, best-effort.
 *   Postgres (predictions)  — authoritative store. Every prediction ever
 *                             generated is kept here forever, enabling the
 *                             accuracy track-record page and the per-user
 *                             prediction-history tab.
 *
 * Read chain (readStoredPrediction):
 *   1. KV hit                  → return (hot path, ~10ms)
 *   2. Postgres hit            → re-populate KV, return (warm path, ~50ms)
 *   3. miss                    → caller (lib/claude.ts) generates fresh
 *
 * Write chain (writeStoredPrediction):
 *   Always write-through: KV + Postgres in parallel. A KV failure never
 *   blocks the Postgres write, because Postgres is the truth. A Postgres
 *   failure is logged but does not block KV (so the user still sees the
 *   prediction immediately — the next cron pass or a manual backfill can
 *   recover the missing row).
 */

import type { Fight, PredictionResult } from "@/types";
import { sql } from "@/lib/db";
import {
  getCachedPrediction,
  setCachedPrediction,
} from "@/lib/cache";

// Bumped whenever the prompt, model, or input signals change in a way that
// invalidates prior predictions. Used as the model_version column in Postgres
// and keeps older predictions around for historical / accuracy comparison.
//
// Current: claude-opus-4-6 (upgraded from sonnet-4 in Task C).
export const MODEL_VERSION = "opus-4-6-v1";

// ─── Read ─────────────────────────────────────────────────────────────────────

/**
 * Look up an existing prediction for this exact matchup + model, preferring
 * the fast KV cache. On KV miss, fall back to Postgres and re-populate KV.
 * Returns null if we've never generated this prediction before.
 */
export async function readStoredPrediction(
  fight: Fight,
): Promise<PredictionResult | null> {
  // 1. KV hot path. Passing MODEL_VERSION hits the v4 key (matchup + model),
  // which guarantees we never serve a prediction generated by an older model
  // after a model bump. v3/v2/v1 are still read as fallbacks inside
  // getCachedPrediction for backwards compat with pre-v4 cached entries.
  const kv = await getCachedPrediction(
    fight.id,
    fight.fighter1.id,
    fight.fighter2.id,
    MODEL_VERSION,
  );
  if (kv) return kv;

  // 2. Postgres fallback.
  try {
    const rows = await sql`
      SELECT prediction
      FROM predictions
      WHERE fight_id      = ${fight.id}
        AND fighter1_id   = ${fight.fighter1.id}
        AND fighter2_id   = ${fight.fighter2.id}
        AND model_version = ${MODEL_VERSION}
      ORDER BY generated_at DESC
      LIMIT 1
    `;
    if (rows.length === 0) return null;

    const prediction = rows[0].prediction as PredictionResult;

    // Warm the KV back up so the next reader gets the fast path.
    // Fire-and-forget: never block the response on cache writes.
    // Write under the v4 key so the follow-up read can find it.
    setCachedPrediction(
      fight.id,
      prediction,
      fight.fighter1.id,
      fight.fighter2.id,
      MODEL_VERSION,
    ).catch(() => { /* ignore */ });

    return prediction;
  } catch (err) {
    console.error("[predictions] Postgres read failed:", err);
    // KV was already checked and missed; if Postgres is down we have to
    // report a miss so the caller can decide to regenerate.
    return null;
  }
}

// ─── Write ────────────────────────────────────────────────────────────────────

export interface PredictionInputsSnapshot {
  // A compact snapshot of the key signals / odds used to make the prediction.
  // Freeform JSONB — schema will evolve. Keep it small (we write one row per
  // matchup per model).
  signals?: unknown;
  marketOdds?: unknown;
  [key: string]: unknown;
}

/**
 * Persist a freshly-generated prediction. Writes to BOTH Postgres (authoritative)
 * and KV (cache). Inputs are optional but recommended — they enable post-hoc
 * accuracy analysis by signal.
 */
export async function writeStoredPrediction(
  fight: Fight,
  prediction: PredictionResult,
  inputs?: PredictionInputsSnapshot,
): Promise<void> {
  // Always write under the v4 key (matchup + model). Any older v3/v2/v1
  // entries will naturally expire; we don't proactively delete them because
  // the read chain in getCachedPrediction prefers v4 anyway.
  const kvWrite = setCachedPrediction(
    fight.id,
    prediction,
    fight.fighter1.id,
    fight.fighter2.id,
    MODEL_VERSION,
  ).catch((err) => {
    console.error("[predictions] KV write failed (non-fatal):", err);
  });

  const pgWrite = persistPredictionRow(fight, prediction, inputs).catch(
    (err) => {
      console.error("[predictions] Postgres write failed (non-fatal):", err);
    },
  );

  // Both are best-effort; we await so that callers can rely on the write
  // having been attempted by the time they return to the HTTP handler.
  await Promise.all([kvWrite, pgWrite]);
}

async function persistPredictionRow(
  fight: Fight,
  prediction: PredictionResult,
  inputs?: PredictionInputsSnapshot,
): Promise<void> {
  // ON CONFLICT DO UPDATE so re-generating the same matchup overwrites the
  // previous row (same model_version). If model_version differs, this is a
  // new row — the UNIQUE index includes model_version.
  await sql`
    INSERT INTO predictions
      (fight_id, fighter1_id, fighter2_id, event_id, model_version, prediction, inputs, generated_at)
    VALUES
      (
        ${fight.id},
        ${fight.fighter1.id},
        ${fight.fighter2.id},
        ${fight.eventId},
        ${MODEL_VERSION},
        ${JSON.stringify(prediction)}::jsonb,
        ${inputs ? JSON.stringify(inputs) : null}::jsonb,
        NOW()
      )
    ON CONFLICT (fight_id, fighter1_id, fighter2_id, model_version)
    DO UPDATE SET
      prediction   = EXCLUDED.prediction,
      inputs       = EXCLUDED.inputs,
      generated_at = NOW()
  `;
}

// ─── View tracking (feeds /my-predictions history tab) ───────────────────────

/**
 * Event-level context captured at view time and denormalized into the view
 * row. Keeps the /my-predictions page self-sufficient (no ESPN/KV re-fetch
 * when rendering a user's history).
 */
export interface ViewEventContext {
  eventName?: string;
  eventDate?: string; // ISO string; optional in case caller doesn't have it
}

/**
 * Record that a user has viewed a prediction. Upserts a row in
 * user_prediction_views, incrementing view_count on repeat views and
 * updating last_viewed_at. Fire-and-forget from the caller's perspective —
 * failures are logged but never block the prediction response.
 *
 * userId can be null/undefined for anonymous views; we currently skip those
 * because we have nothing to key them on, but the schema admits NULL user_id
 * so global/anonymous tracking can be added later without migration.
 *
 * `eventCtx` carries event name/date so the /my-predictions page can render
 * readable rows without re-fetching. Safe to omit — columns are nullable.
 */
export async function recordPredictionView(
  fight: Fight,
  userId: string | null | undefined,
  eventCtx?: ViewEventContext,
): Promise<void> {
  if (!userId) return; // Anonymous views: skip for now.

  try {
    await sql`
      INSERT INTO user_prediction_views
        (user_id, fight_id, fighter1_id, fighter2_id, event_id, model_version,
         first_viewed_at, last_viewed_at, view_count,
         fighter1_name, fighter2_name, weight_class,
         event_name, event_date, is_title_fight, is_main_event)
      VALUES
        (
          ${userId},
          ${fight.id},
          ${fight.fighter1.id},
          ${fight.fighter2.id},
          ${fight.eventId},
          ${MODEL_VERSION},
          NOW(),
          NOW(),
          1,
          ${fight.fighter1.name},
          ${fight.fighter2.name},
          ${fight.weightClass},
          ${eventCtx?.eventName ?? null},
          ${eventCtx?.eventDate ?? null},
          ${fight.isTitleFight},
          ${fight.isMainEvent}
        )
      ON CONFLICT (user_id, fight_id, fighter1_id, fighter2_id, model_version)
      DO UPDATE SET
        last_viewed_at = NOW(),
        view_count     = user_prediction_views.view_count + 1,
        -- Refresh the display snapshot on repeat views in case event metadata
        -- changed (renamed event, rescheduled date, etc.). Cheap and keeps
        -- history looking correct without a cron backfill.
        fighter1_name  = EXCLUDED.fighter1_name,
        fighter2_name  = EXCLUDED.fighter2_name,
        weight_class   = EXCLUDED.weight_class,
        event_name     = COALESCE(EXCLUDED.event_name,  user_prediction_views.event_name),
        event_date     = COALESCE(EXCLUDED.event_date,  user_prediction_views.event_date),
        is_title_fight = EXCLUDED.is_title_fight,
        is_main_event  = EXCLUDED.is_main_event
    `;
  } catch (err) {
    // Non-fatal: history tracking is nice-to-have, never block a prediction
    // response on a failed INSERT. A failed write just means this one view
    // doesn't appear in the user's history — the next view will upsert fine.
    console.error("[predictions] recordPredictionView failed (non-fatal):", err);
  }
}

export interface UserPredictionViewRow {
  userId: string;
  fightId: string;
  fighter1Id: string;
  fighter2Id: string;
  fighter1Name: string | null;
  fighter2Name: string | null;
  weightClass: string | null;
  eventId: string | null;
  eventName: string | null;
  eventDate: string | null;
  isTitleFight: boolean;
  isMainEvent: boolean;
  modelVersion: string;
  firstViewedAt: string;
  lastViewedAt: string;
  viewCount: number;
  // The joined prediction payload — null if the prediction row was deleted
  // or if model_version diverged (shouldn't happen in practice, but keeping
  // the guard for safety).
  prediction: PredictionResult | null;
}

/**
 * Per-user history: the user's most-recently viewed predictions, newest first,
 * joined with the prediction payload from the `predictions` table. Denormalized
 * display columns (fighter names, event name/date, weight class) come straight
 * from the view row — no ESPN/KV re-fetch required.
 */
export async function listUserPredictionViews(
  userId: string,
  limit = 50,
): Promise<UserPredictionViewRow[]> {
  const rows = await sql`
    SELECT
      v.user_id,
      v.fight_id,
      v.fighter1_id,
      v.fighter2_id,
      v.fighter1_name,
      v.fighter2_name,
      v.weight_class,
      v.event_id,
      v.event_name,
      v.event_date,
      v.is_title_fight,
      v.is_main_event,
      v.model_version,
      v.first_viewed_at,
      v.last_viewed_at,
      v.view_count,
      p.prediction
    FROM user_prediction_views v
    LEFT JOIN predictions p
      ON p.fight_id      = v.fight_id
     AND p.fighter1_id   = v.fighter1_id
     AND p.fighter2_id   = v.fighter2_id
     AND p.model_version = v.model_version
    WHERE v.user_id = ${userId}
    ORDER BY v.last_viewed_at DESC
    LIMIT ${limit}
  `;
  return rows.map((r: any) => ({
    userId:        r.user_id as string,
    fightId:       r.fight_id as string,
    fighter1Id:    r.fighter1_id as string,
    fighter2Id:    r.fighter2_id as string,
    fighter1Name:  (r.fighter1_name as string) ?? null,
    fighter2Name:  (r.fighter2_name as string) ?? null,
    weightClass:   (r.weight_class as string) ?? null,
    eventId:       (r.event_id as string) ?? null,
    eventName:     (r.event_name as string) ?? null,
    eventDate:     r.event_date ? new Date(r.event_date as string).toISOString() : null,
    isTitleFight:  Boolean(r.is_title_fight),
    isMainEvent:   Boolean(r.is_main_event),
    modelVersion:  r.model_version as string,
    firstViewedAt: new Date(r.first_viewed_at as string).toISOString(),
    lastViewedAt:  new Date(r.last_viewed_at as string).toISOString(),
    viewCount:     r.view_count as number,
    prediction:    (r.prediction as PredictionResult) ?? null,
  }));
}

// ─── Accuracy / history helpers (used by /api/accuracy, /api/history) ─────────

export interface StoredPredictionRow {
  id: number;
  fightId: string;
  fighter1Id: string;
  fighter2Id: string;
  eventId: string | null;
  modelVersion: string;
  prediction: PredictionResult;
  generatedAt: string;
}

/**
 * List all predictions for a given event (for the event-level history view).
 */
export async function listPredictionsForEvent(
  eventId: string,
): Promise<StoredPredictionRow[]> {
  const rows = await sql`
    SELECT id, fight_id, fighter1_id, fighter2_id, event_id,
           model_version, prediction, generated_at
    FROM predictions
    WHERE event_id = ${eventId}
    ORDER BY generated_at DESC
  `;
  return rows.map((r: any) => ({
    id:           r.id as number,
    fightId:      r.fight_id as string,
    fighter1Id:   r.fighter1_id as string,
    fighter2Id:   r.fighter2_id as string,
    eventId:      (r.event_id as string) ?? null,
    modelVersion: r.model_version as string,
    prediction:   r.prediction as PredictionResult,
    generatedAt:  new Date(r.generated_at as string).toISOString(),
  }));
}
